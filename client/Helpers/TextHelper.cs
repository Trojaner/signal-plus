    public static Color24[,] CreateText(string message, Color24? color = null)
    {
        var textline = System.Text.Encoding.ASCII.GetBytes(message);
        var screenimage = new Color24[textline.Length * 6, 8];

        for (var x = 0; x < textline.Length * 6; x++)
        {
            if ((x % 6) == 5)
            {
                for (var y = 0; y < 8; y++)
                {
                    screenimage[x, y] = Color24.Black;
                }
            }
            else
            {
                for (var y = 0; y < 8; y++)
                {
                    if (((Font5x8[textline[x / 6], x % 6] >> y) & 0x01) > 0)
                    {
                        if (color != null)
                        {
                            screenimage[x, y] = color.Value;
                        }
                        else
                        {
                            var colorphase = x / 12.0 * 2.0 * Math.PI + y / 8.0 * 2.0 * Math.PI;
                            screenimage[x, y] = new Color24((byte)(254.0 * (.5 + .5 * Math.Sin(colorphase))),
                                (byte)(254.0 * (.5 + .5 * Math.Sin(colorphase + 2.0 * (1.0 / 3.0) * Math.PI))),
                                (byte)(254.0 * (.5 + .5 * Math.Sin(colorphase + 2.0 * (2.0 / 3.0) * Math.PI))));
                        }
                    }
                    else
                    {
                        // If the font says no, draw black.
                        screenimage[x, y] = Color24.Black;
                    }
                }
            }
        }

        return screenimage;
    }

